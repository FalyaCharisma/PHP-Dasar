<?php
    // Operator Aritmatika
    // +, -, *, /, %
    $a = 5;
    $b = 2;
    echo $a + $b . "<br>";
    echo $a - $b . "<br>";
    echo $a * $b . "<br>";
    echo $a / $b . "<br>";
    echo $a % $b . "<br>";

    // Menggabungkan teks
    // .
    $nama_depan = "Falya";
    $nama_belakang = "Ayza";
    echo $nama_depan . " " . $nama_belakang;

    // Operator Assignment / Penugasan
    // =, +=, -=, *=, /=, %= .=
    $x = 1;
    $x += 5;
    echo $x;

    // Operator Perbandingan
    // <, >, <=, >=, ==, !=
    var_dump($a == $b);

    // Operator Identitas
    // ===, !==
    var_dump(2 === "2");

    // Operator Logika
    // && and, || or
    var_dump( 2 > 1 || 1 < 2);
?>